list_data = [1, 2, 3, 4, 5]
print(type(list_data)) # Output: <class 'list'>
print(list_data)       # Output: [1, 2, 3, 4, 5]
print(len(list_data))  # Output: 5  

print(list_data[1])    # Output: 2
list_data.append(6)
print(list_data)    # Output: [1, 2, 3, 4, 5, 6]
print.remove(3)
print(list_data)    # Output: [1, 2, 4, 5]
print(sum(list_data)) # Output: 12

